package com.my.app.task;

public class UserInfo { 
	
	private String info_nickname;
  	private String info_gender;
  	private String info_province;
  	private String info_city;
  	private String info_photo;
  	private String info_sign;
	  
	public String getInfo_nickname() {
		return info_nickname;
	}
	public void setInfo_nickname(String info_nickname) {
		this.info_nickname = info_nickname;
	}
	public String getInfo_gender() {
		return info_gender;
	}
	public void setInfo_gender(String info_gender) {
		this.info_gender = info_gender;
	}
	public String getInfo_province() {
		return info_province;
	}
	public void setInfo_province(String info_province) {
		this.info_province = info_province;
	}
	public String getInfo_city() {
		return info_city;
	}
	public void setInfo_city(String info_city) {
		this.info_city = info_city;
	}
	public String getInfo_photo() {
		return info_photo;
	}
	public void setInfo_photo(String info_photo) {
		this.info_photo = info_photo;
	}
	public String getInfo_sign() {
		return info_sign;
	}
	public void setInfo_sign(String info_sign) {
		this.info_sign = info_sign;
	}
}

 
